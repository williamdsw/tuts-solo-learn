#include <iostream>

using namespace std;

int main1()
{
	int a = 1, b = 2;
	
	if(a > b)
	{
		cout << "a é maior" << endl;
	}
	
	if(a < b)
	{
		cout << "b é maior" << endl;
	}
	
	if(a == b)
	{
		cout << "a e b são iguais" << endl;
	}
	
	if(a != b)
	{
		cout << "a e b são diferentes" << endl;
	}
	
	if(a >= b)
	{
		cout << "a é maior ou igual" << endl;
	}
	
	if(a <= b)
	{
		cout << "a é menor ou igual" << endl;
	}
		
	return 0;
}
