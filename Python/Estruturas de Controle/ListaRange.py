#FUNÇÃO "RANGE()" PARA LIST

#variáveis
#range(valor) = vai criar uma sequência padrão de 0 até valor
numeros = list(range(10))

print(numeros)

#utilizando "range()" com parâmetro de ínicio e final = 3,4,5,6,7,8
numeros = list(range(3, 9))

print(numeros)

#utilizando "range()" com parâmetro de ínicio e fim, pulando de 2 em 2 = 5,7,9,11,13
numeros = list(range(5, 15, 2))

print(numeros)
